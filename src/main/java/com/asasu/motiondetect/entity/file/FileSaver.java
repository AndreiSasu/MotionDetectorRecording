package com.asasu.motiondetect.entity.file;

public class FileSaver {
	private String fileSaverName;
	private String credentialToken;
	private Long id;

	public String getFileSaverName() {
		return fileSaverName;
	}

	public void setFileSaverName(String fileSaverName) {
		this.fileSaverName = fileSaverName;
	}

	public String getCredentialToken() {
		return credentialToken;
	}

	public void setCredentialToken(String credentialToken) {
		this.credentialToken = credentialToken;
	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

}
